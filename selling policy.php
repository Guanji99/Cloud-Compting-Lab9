            <h2>SELLER ACCOUNTS</h2>
            <h3>HOW DO I SELL ON SNKRHead?</h3>
            <p>To ensure quality and authentic items, we only allow a select group of sellers. We encourage you to request to become a seller on SNKRHeadT by tapping on the Sell tab on the app and filling out the required information. We’ll notify you as soon as your request has been approved.</p>
            <h3>I REQUESTED TO SELL. WHEN WILL MY ACCOUNT BE APPROVED?</h3>
            <p>We appreciate your interest in selling on SNKRHead. We are currently approving sneaker sellers on a rolling basis and a limited number of apparel and accessory sellers. We do this to ensure we provide the best possible experience for our sellers.</p>
            <h3>CAN I SHARE A SELLER ACCOUNT?</h3>
            <p>Users may not share seller accounts. Users who are found to be sharing seller accounts may be suspended from selling.</p>
            <h3>CAN I CREATE MORE THAN ONE SELLER ACCOUNT?</h3>
            <p>Each user is only allowed to create one seller account. Users who are found to have created more than one seller account may be suspended from selling.</p>
            <br><h2>LISTINGS</h2>
            <h3>HOW DO I LIST ITEMS FOR SALE?</h3>
            <p>After your account is approved for selling, you can tap the Sell tab to list your items. Start by searching for the SKU, which can be found on the box or inside the item, enter the condition, price and take the required photos. Sellers listing items that are used or new with defects or who have a seller rating below 100 are required to take photos.</p>
            <h3>WHY ARE MY PHOTOS BEING REJECTED?</h3>
            <p>We require clear photos that follow our photo guidelines. The photos you upload must be of the items in your possession that you intend to sell and ship. Any issues such as damage, discoloration, or flaws should be clearly photographed.</p>
            <p>Here are some additional common reasons for photo rejections:</p>
            <p style="padding-left: 30px;">-Both sneakers are not shown in a full, clear view. <br>Both products are not fully visible within the frame of the photo. <br>-Low lighting. We recommend taking photos in bright lighting conditions, such as under direct sunlight.
            <br>-Details (e.g., SKU, size, etc.) are not fully readable in box/ tag photo. <br>-Personal information shown in photos.</p>
            <h3>HOW DO I EDIT MY LISTING?</h3>
            <p>Tap the Sell tab, then tap Listings to select the items you’d like to edit. Once you’ve selected the items, you will be taken to your listing, where you can edit the size, condition, price, photos and input any defects. Please make sure to save your changes.</p>
